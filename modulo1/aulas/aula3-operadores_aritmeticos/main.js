
// Operadores Aritméticos e operações matemáticas

var numero1 = 10, numero2 = 2;

console.log('Número 1:', numero1, 'Número 2:', numero2);

console.log('Soma:', numero1 + numero2);
console.log('Subtração:', numero1 - numero2);
console.log('Multiplicação:', numero1 * numero2);
console.log('Divisão:', numero1 / numero2);
console.log('Resto da divisão:', numero1 / numero2);


// Incrementando números a um valor atual no JS

numero1 += 5;         // Mesma coisa que:        numero1 = numero1 + 5;
console.log(numero1);


// Decrementando números a um valor atual no JS

numero2 -= 1          // Mesma coisa que:        numero2 = numero2 + 1;
console.log(numero2);


// Podemos utilizar qualquer operador

numero2 = 2;
numero2 *= 2          // Mesma coisa que:        numero2 = numero2 * 2;
console.log(numero2);

numero1 = 10;
numero1 /= 5          // Mesma coisa que:        numero2 = numero2 / 5;
console.log(numero1);
